function loadEventDetails() {
    // Get the event category from the URL parameter
    const urlParams = new URLSearchParams(window.location.search);
    const category = urlParams.get("event");

    // Load event details
    const event = eventData[category];

    if (event) {
        document.getElementById("event-description").innerHTML = event.description;
        document.getElementById("event-banner").src = event.image;
    } else {
        document.getElementById("event-title").innerText = "Event Not Found";
        document.getElementById("event-description").innerHTML = "<p>The event details you are looking for do not exist.</p>";
    }
}

